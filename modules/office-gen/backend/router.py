SUPPORTED_GENERATE_FORMATS = {"docx", "xlsx", "pptx", "pdf"}


def _normalize_format(format_type: object) -> str:
    fmt = str(format_type or "").lower().lstrip(".").strip()
    if fmt not in SUPPORTED_GENERATE_FORMATS:
        raise ValidationError(f"Unsupported format: {fmt or '<empty>'}")
    return fmt


def _normalize_convert_format(format_type: object) -> str:
    fmt = str(format_type or "").lower().lstrip(".").strip()
    if not fmt:
        raise ValidationError("target_format is required")
    if fmt not in converter.SUPPORTED_FORMATS:
        supported = ", ".join(sorted(converter.SUPPORTED_FORMATS))
        raise ValidationError(f"Unsupported target_format: {fmt}. Supported: {supported}")
    return fmt


def _positive_int(value: object, name: str) -> int:
    if isinstance(value, bool):
        raise ValidationError(f"{name} must be a positive integer")
    try:
        parsed = int(value)
    except (TypeError, ValueError) as exc:
        raise ValidationError(f"{name} must be a positive integer") from exc
    if parsed <= 0:
        raise ValidationError(f"{name} must be a positive integer")
    return parsed


def _optional_positive_int(value: object, name: str) -> int | None:
    if value is None or value == "":
        return None
    return _positive_int(value, name)


def _require_non_empty_list(params: dict, key: str, aliases: tuple[str, ...] = ()) -> list:
    for candidate in (key, *aliases):
        value = params.get(candidate)
        if isinstance(value, list) and value:
            return value
        content_ir = params.get("content_ir")
        if isinstance(content_ir, dict):
            value = content_ir.get(candidate)
            if isinstance(value, list) and value:
                return value
    raise ValidationError(f"{key} must be a non-empty array")


def _render_generated_bytes(render) -> bytes:
    try:
        return render()
    except (RuntimeError, ValueError) as exc:
        raise ValidationError(str(exc)) from exc


def _generate_bytes_for_format(format_type: str, filename: str, params: dict) -> bytes:
    fmt = _normalize_format(format_type)
    if fmt == "xlsx":
        sheets = _require_non_empty_list(params, "sheets", ("工作表", "blocks"))
        return _render_generated_bytes(lambda: generator.generate_xlsx({"filename": filename, "sheets": sheets}))
    if fmt == "docx":
        content = _require_non_empty_list(params, "content", ("blocks",))
        return _render_generated_bytes(lambda: generator.generate_docx({"filename": filename, "content": content}))
    if fmt == "pptx":
        slides = _require_non_empty_list(params, "slides", ("幻灯片", "blocks"))
        return _render_generated_bytes(lambda: generator.generate_pptx({"filename": filename, "slides": slides}))
    content = _require_non_empty_list(params, "content", ("blocks",))
    return _render_generated_bytes(lambda: generator.generate_pdf({"filename": filename, "content": content}))


async def _ensure_content_package(
    file_id: int,
    owner_id: int,
    caller: str,
    *,
    origin_type: str = "generated",
    db=None,
) -> dict:
    """Best-effort Content Package sync with explicit status in the response."""
    package_id: int | None = None
    owns_session = db is None
    if owns_session:
        db_cm = AsyncSessionLocal()
        db = await db_cm.__aenter__()
    else:
        db_cm = None
    try:
        from app.services.content.package_service import ContentPackageService
        from app.services.content.pipeline_service import ContentPipelineService

        pkg_svc = ContentPackageService()
        pipeline_svc = ContentPipelineService()
        pkg = await pkg_svc.get_or_create(db, file_id, owner_id, caller, origin_type=origin_type)
        package_id = int(pkg["id"])
        result = await pipeline_svc.run_pipeline_for_package(db, pkg["id"], caller)
        return {
            "content_package_id": package_id,
            "content_package_status": result.get("status", pkg.get("status", "unknown")),
            "content_package_error": None,
        }
    except Exception as e:
        logger.warning("Content Package sync failed for file_id=%s: %s", file_id, e)
        return {
            "content_package_id": package_id,
            "content_package_status": "failed",
            "content_package_error": str(e),
        }
    finally:
        if owns_session and db_cm is not None:
            await db_cm.__aexit__(None, None, None)


async def _find_accessible_content_package(db, file_id: int, owner_id: int) -> int | None:
    try:
        from app.services.content.package_service import ContentPackageService

        pkg = await ContentPackageService().get_package(db, file_id=file_id, owner_id=owner_id)
        return int(pkg["id"])
    except Exception:
        return None


async def _save_file_bytes(
    content: bytes,
    filename: str,
    extension: str,
    owner_id: int,
    folder_id: int | None = None,
) -> dict:
    """Save generated bytes via framework file_upload_service (content-addressed)."""
    ext = extension.lower().lstrip(".")
    final_name = f"{filename}.{ext}"

    async with AsyncSessionLocal() as db:
        result = await upload_file(
            db,
            io.BytesIO(content),
            final_name,
            owner_id,
            folder_id,
        )

    return {
        "file_id": result["id"],
        "name": result["name"],
        "extension": result["extension"],
        "size": result["size"],
        "mime_type": result["mime_type"],
        "storage_path": "",
        "deduplicated": result.get("deduplicated", False),
    }


async def _save_to_artifact(
    content: bytes,
    filename: str,
    extension: str,
    owner_id: int,
    folder_id: int | None = None,
) -> dict:
    """Save generated bytes as an artifact (avoids file-name conflicts)."""
    ext = extension.lower().lstrip(".")
    async with AsyncSessionLocal() as db:
        artifact = await create_artifact(
            db, owner_id,
            name=filename,
            extension=ext,
            content=content,
            folder_id=folder_id,
            source_module="office-gen",
            conflict_policy="create_version",
        )
    return artifact


