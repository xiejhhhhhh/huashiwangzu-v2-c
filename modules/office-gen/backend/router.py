# Capability: office-gen:export_to_artifact
# Convert an existing file to an artifact
# =====================================================================
async def _cap_export_to_artifact(params: dict, caller: str) -> dict:
    """Export an existing office file as an artifact record for version management."""
    owner_id = resolve_caller_user_id(caller)
    file_id = _positive_int(params.get("file_id"), "file_id")

    async with AsyncSessionLocal() as db:
        from app.services.file_service import check_file_access

        file = await check_file_access(db, file_id, owner_id)
        content_package_id = await _find_accessible_content_package(db, file_id, owner_id)

    content_bytes = await get_file_content_bytes(file_id, owner_id)

    async with AsyncSessionLocal() as db:
        artifact = await create_artifact(
            db, owner_id,
            name=file.name,
            extension=file.extension or "",
            content=content_bytes,
            folder_id=file.folder_id,
            source_module="office-gen",
            source_object_type="content_package" if content_package_id else "file",
            source_object_id=content_package_id or file_id,
            file_id=file_id,
            conflict_policy="create_version",
        )

    return {
        "artifact_id": artifact["id"],
        "file_id": file_id,
        "content_package_id": content_package_id,
        "name": artifact["name"],
        "extension": artifact["extension"],
        "size": artifact["size"],
    }


