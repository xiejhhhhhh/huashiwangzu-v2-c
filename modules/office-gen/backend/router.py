# Capability: office-gen:generate_to_artifact
# Generate Office file and return as artifact (avoids file-name conflicts)
# =====================================================================
async def _cap_generate_to_artifact(params: dict, caller: str) -> dict:
    """Generate an office file and return as an artifact record.
    No file-name conflict: artifacts support create_version mode by default.
    """
    owner_id = resolve_caller_user_id(caller)
    format_type = _normalize_format(params.get("format", "xlsx"))
    filename = params.get("filename", "未命名文档")
    folder_id = _optional_positive_int(params.get("folder_id"), "folder_id")

    bytes_data = _generate_bytes_for_format(format_type, filename, params)

    artifact = await _save_to_artifact(bytes_data, filename, format_type, owner_id, folder_id)
    artifact_file_id = artifact.get("file_id")
    if not artifact_file_id:
        raise RuntimeError("Artifact was created without a file_id")

    package_state = await _ensure_content_package(
        int(artifact_file_id),
        owner_id,
        caller,
        origin_type="generated",
    )

    return {
        "artifact_id": artifact["id"],
        "file_id": artifact.get("file_id"),
        **package_state,
        "format": format_type,
        "name": artifact["name"],
        "extension": artifact["extension"],
        "size": artifact["size"],
        "status": "created",
    }


